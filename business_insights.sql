-- ❓ Business Problem  :- Company does not know which category contributes most to revenue.
SELECT 
    category,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM cleaned_dataset
GROUP BY category
ORDER BY total_revenue DESC;

-- 🚀 Key Insight: Revenue is heavily concentrated in Electronics at 480,210.81, 
-- which significantly outperforms other categories, while Outerwear contributes the least at just 18,558.00, 
-- indicating a substantial performance gap across the product portfolio. 
			
---- ❓ Business Problem  2:  Are discounts actually increasing purchase value?
SELECT 
    discount_applied,
    ROUND(SUM(purchase_amount), 2) AS total_revenue,
    ROUND(AVG(purchase_amount), 2) AS avg_purchase
FROM cleaned_dataset
GROUP BY discount_applied;

-- 🚀 Key Insight: While total revenue is slightly higher when no discount is applied (502,562.72 vs. 492,207.59), 
-- applying a discount significantly increases the average purchase value from 180.19 to 217.69,
--  suggesting that discounts successfully encourage customers to spend more per transaction.

-- ❓ Business Problem : 3. What is the total revenue generated by male and female customer ? 
SELECT 
    gender,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM cleaned_dataset
