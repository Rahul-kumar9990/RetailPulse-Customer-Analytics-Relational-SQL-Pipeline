-- 🚀 Key Insight: Revenue is fairly distributed across all segments, with Male customers generating the highest revenue at 392,443.71,
-- followed closely by Female customers at 352,269.39, and the Other category contributing 250,057.21.
 
 -- ❓ Business Problem  4. Which customer used a discount but still spent more then the average purchase amount ?
SELECT 
    customer_id,
    purchase_amount,
    discount_applied
FROM cleaned_dataset
WHERE discount_applied = 'Yes' 
  AND purchase_amount > (SELECT AVG(purchase_amount) FROM cleaned_dataset)
ORDER BY purchase_amount DESC
LIMIT 10;
-- 🚀 Key Insight: Identifying high-value customers, such as customer 4977 who spent 1,499.76, 
-- reveals a segment of shoppers who utilize discounts but still consistently generate purchase amounts significantly above the average,
--  representing a prime target for loyalty-building programs.

