-- ❓ Business Problem: 6. Average purchase: Standard vs Express shipping
SELECT 
    shipping_type,
    COUNT(DISTINCT customer_id) AS total_orders,
    ROUND(AVG(purchase_amount), 2) AS avg_order_value,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM cleaned_dataset
GROUP BY shipping_type
ORDER BY total_revenue DESC;

--- Key Insight: Faster shipping methods appear to correlate with higher spending, 
-- as Express shipping leads with the highest average order value of 354.72 and total revenue of 426,730.45, 
-- while options like Free Shipping and Next Day Air have significantly lower average order values, approximately 60.46 and 58.62 respectively.


            
 --  Business Problem  7. Do subscribed customers spend more ? Compare average speed and total revenue between subscribers and non-subscribers.
SELECT 
    subscription_status,
    COUNT(customer_id) AS total_users,
    ROUND(AVG(purchase_amount), 2) AS avg_revenue,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM cleaned_dataset
GROUP BY subscription_status
ORDER BY total_revenue DESC;

---- Key Insight: While non-subscribed customers generate a higher total revenue of 569,195.30 due to a larger user base, 
-- subscribed customers are significantly more valuable on an individual basis,
--  with a substantially higher average revenue per purchase of 269.86 compared to 163.89 for non-subscribers.         


--  ❓ Business Problem 8. Top 5 products with highest discount usage %
SELECT 
    item_purchased,
    COUNT(*) AS total_sold,
    SUM(CASE WHEN discount_applied = 'Yes' THEN 1 ELSE 0 END) AS times_discounted,
    ROUND(SUM(CASE WHEN discount_applied = 'Yes' THEN 1 ELSE 0 END) * 100.0 / COUNT(*), 2) AS discount_usage_percent
FROM cleaned_dataset
GROUP BY item_purchased
ORDER BY discount_usage_percent DESC
LIMIT 5;

----- Key Insight: The products with the highest reliance on discounts are led by Laptop at 52.86%, 
-- followed by Headphones at 51.81%, Sneakers at 50.70%, Phone at 50.59%, and Hat at 50.33%, 
-- highlighting that these items are frequently purchased using promotional offers.

            
-- ❓ Business Problem 9. Segment customer into new, returning and loyal based on their total number of previous purchase, 
-- and show the count of each segment.
SELECT 
    CASE 
        WHEN previous_purchases = 0 THEN 'New Customer'
        WHEN previous_purchases BETWEEN 1 AND 15 THEN 'Returning Customer'
        ELSE 'Loyal Customers'
    END AS customer_segment,
    COUNT(*) AS customer_count
FROM cleaned_dataset
GROUP BY CASE 
        WHEN previous_purchases = 0 THEN 'New Customer'
        WHEN previous_purchases BETWEEN 1 AND 15 THEN 'Returning Customer'
        ELSE 'Loyal Customers'
    END;
    
---- Key Insight: The customer base is predominantly composed of Loyal Customers with a count of 3,116,
--  significantly outnumbering Returning Customers at 1,373 and New Customers at 561, 
--  which indicates a strong base of long-term engagement with the brand.
 
            
-- ❓ Business Problem: 10. What are the top 3 most purchased products within each category ?
WITH CategoryRanking AS (
    SELECT 
        category,
        item_purchased,
        COUNT(*) AS purchase_count,
        RANK() OVER (PARTITION BY category ORDER BY COUNT(*) DESC) AS rnk
    FROM cleaned_dataset
    GROUP BY category, item_purchased
)
SELECT * FROM CategoryRanking WHERE rnk <= 3;

-- Key Insight: Identifying the top 3 most purchased items per category reveals clear volume leaders, 
-- such as Shirt in Clothing (370 purchases), Shoes in Footwear (305 purchases), Jewelry in Accessories (180 purchases), 
-- and Phone in Electronics (170 purchases), which can directly inform inventory and marketing strategies.
            
-- ❓ Business Problem: 11. Are customers who are repeat buyers (more than 5 previous purchases) also likely to subscribe ?
SELECT
    CASE 
        WHEN previous_purchases > 5 THEN 'Repeat Buyers'
        ELSE 'Normal Buyers'
    END AS buyer_type,
    subscription_status,
    COUNT(*) AS customer_count,
    ROUND(COUNT(*) * 100.0 / SUM(COUNT(*)) OVER (PARTITION BY CASE 
                                        WHEN previous_purchases > 5 THEN 'Repeat Buyers'
                                        ELSE 'Normal Buyers'
                                    END), 2) AS percentage
FROM cleaned_dataset
GROUP BY CASE 
        WHEN previous_purchases > 5 THEN 'Repeat Buyers'
        ELSE 'Normal Buyers'
    END, subscription_status;

-- 🚀 Key Insight: Interestingly, Normal Buyers show a higher propensity to subscribe at 37.25% compared to Repeat Buyers, 
-- of whom only 29.64% have a subscription, suggesting that current subscription incentives may be less 
-- effective at retaining highly active repeat customers.
		
            
-- ❓ Business Problem: 12. What is the revenue contribution of each age group ? 
SELECT 
    CASE 
        WHEN age BETWEEN 18 AND 25 THEN '18-25'
        WHEN age BETWEEN 26 AND 35 THEN '26-35'
        WHEN age BETWEEN 36 AND 50 THEN '36-50'
        ELSE '51+'
    END AS age_group,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM cleaned_dataset
GROUP BY CASE 
        WHEN age BETWEEN 18 AND 25 THEN '18-25'
        WHEN age BETWEEN 26 AND 35 THEN '26-35'
        WHEN age BETWEEN 36 AND 50 THEN '36-50'
        ELSE '51+'
    END
ORDER BY total_revenue DESC;

--- Key Insight: The 51+ age group is the highest revenue contributor at 399,214.36, followed by the 36-50 age group at 266,355.41,
--  the 26-35 age group at 178,098.38, and the 18-25 age group at 151,102.15, 
--  demonstrating that older demographics provide the most significant share of total revenue.


